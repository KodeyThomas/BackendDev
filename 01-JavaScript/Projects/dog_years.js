var myAge = 17; // My Age

var earlyYears = 2; // Early Years

earlyYears *= 10.5;

laterYears = myAge - 2; // We have already accounted for first years

laterYears *= 4;

myAgeInDogYears = earlyYears + laterYears

myName = 'Kodey Thomas'.toLowerCase(); // Converts to lower case and creates variable called myName

console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`)